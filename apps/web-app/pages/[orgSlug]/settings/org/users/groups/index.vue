<script setup lang="ts">
  import { navigateTo, ref, useNuxtApp, useRoute, watch } from '#imports';

  const { $trpc } = useNuxtApp();
  const orgSlug = useRoute().params.orgSlug as string;

  const {
    data: orgGroupsQuery,
    pending,
    refresh
  } = await $trpc.org.users.groups.getOrgGroups.useLazyQuery(
    {},
    {
      server: false
    }
  );

  const tableColumns = [
    {
      key: 'name',
      label: 'Name',
      sortable: true
    },
    {
      key: 'description',
      label: 'Description',
      sortable: true
    },
    {
      key: 'members',
      label: 'Members'
    }
  ];

  interface TableRow {
    publicId: string;
    avatarId: string;
    name: string;
    description: string | null;
    color: string | null;
    members: ({
      publicId: string;
      avatarId: string;
      firstName: string | null;
      lastName: string | null;
      handle: string | null;
      title: string | null;
    } | null)[];
  }

  const tableRows = ref<TableRow[]>([]);
  watch(orgGroupsQuery, (newResults) => {
    if (newResults?.groups) {
      tableRows.value = [];
      for (const group of newResults.groups) {
        tableRows.value.push({
          publicId: group.publicId,
          avatarId: group.avatarId || '',
          name: group.name,
          description: group.description,
          color: group.color,
          // @ts-ignore
          members: group.members
        });
      }
    }
  });
  function select(row: (typeof tableRows.value)[number]) {
    navigateTo(`/${orgSlug}/settings/org/users/groups/${row.publicId}`);
  }

  const addNewModalOpen = ref(false);
  const closeModal = () => {
    addNewModalOpen.value = false;
    refresh();
  };
</script>

<template>
  <div class="flex h-full w-full flex-col items-start gap-8 p-4">
    <div class="flex w-full flex-row items-center justify-between">
      <div class="flex flex-row items-center gap-4">
        <div class="flex flex-col gap-1">
          <span class="font-display text-2xl">Groups</span>
          <span class="text-sm">Manage your organizations user groups</span>
        </div>
      </div>
      <div class="flex flex-row items-center gap-4">
        <UnUiButton
          label="Add new"
          @click="addNewModalOpen = true" />
        <UnUiModal v-model="addNewModalOpen">
          <template #header>
            <span class="">Add new group</span>
          </template>
          <SettingsAddNewGroup
            lazy
            @close="closeModal()" />
        </UnUiModal>
      </div>
    </div>
    <div class="flex w-full flex-col gap-4 overflow-y-auto">
      <NuxtUiTable
        :columns="tableColumns"
        :rows="tableRows"
        :loading="pending"
        class=""
        @select="select">
        <template #name-data="{ row }">
          <div class="flex flex-row items-center gap-2">
            <UnUiAvatar
              :public-id="row.publicId"
              :avatar-id="row.avatarId"
              :type="'orgMember'"
              :alt="row.name ? row.name : ''"
              :color="row.color ? row.color : ''"
              size="xs" />
            <span class="">{{ row.name }}</span>
          </div>
        </template>
        <template #members-data="{ row }">
          <div v-if="row.members.length > 0">
            <NuxtUiAvatarGroup
              size="sm"
              :max="3">
              <UnUiAvatar
                v-for="member in row.members"
                :key="member.publicId"
                :public-id="member.orgMember.publicId"
                :avatar-id="member.orgMemberProfile.avatarId"
                :type="'orgMember'"
                :alt="
                  member.orgMemberProfile.firstName &&
                  member.orgMemberProfile.lastName
                    ? member.orgMemberProfile.firstName +
                      ' ' +
                      member.orgMemberProfile.lastName
                    : ''
                "
                :color="
                  member.orgMemberProfile.color
                    ? member.orgMemberProfile.color
                    : ''
                "
                size="xs" />
            </NuxtUiAvatarGroup>
          </div>

          <div v-if="!row.members">
            <span class="text-xs">No Members</span>
          </div>
        </template>
      </NuxtUiTable>
    </div>
  </div>
</template>
